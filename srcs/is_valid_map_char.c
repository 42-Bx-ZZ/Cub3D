#include "../inc/cub3d.h"

int	is_valid_map_char(char c)
{
	return (c == 'E' || c == 'W' || c == 'S' || c == 'N' || c == 'Z'
		|| c == 'D' || c == ' ' || c == '1' || c == '0');
}
